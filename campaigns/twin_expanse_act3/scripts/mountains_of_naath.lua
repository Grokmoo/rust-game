function on_area_load(parent)
  game:set_world_map_location_visible("mountains_of_naath", true)
  game:set_world_map_location_enabled("mountains_of_naath", true)
  game:set_world_map_location_visible("naathfir", true)
  game:set_world_map_location_enabled("naathfir", true)
  game:warn("set")
end